
class summing
{
    a : number = 71;
    b : number = 84;
    add() : void
    {
        console.log("The Addition of A and B is : " +(this.a+this.b))
    }
}

let M = new summing();
M.add();

class diff
{
    j : number = 92;
    k : number = 55;
    substract() : void
    {
        console.log("The Difference of J and K is : " +(this.j-this.k));
    }
}

let N = new diff();
N.substract();

